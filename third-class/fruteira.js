function fruteira(fruta){

    switch (fruta) {
       case "banana":
          console.log("Banana existe na fruteira");
          break;
       case "maçã":
          console.log("Maçã existe na fruteira");
          break;
       case "manga":
          console.log("Manga existe na fruteira");
          break;
       case "pêra":
          console.log("Pêra existe na fruteira");
          break;
       case "pêssego":
          console.log("Pêssego existe na fruteira");
           break;
       default:
          console.log("A fruta " + fruta + " não existe na fruteira");
    }
}

fruta = "banana";
fruteira(fruta);
